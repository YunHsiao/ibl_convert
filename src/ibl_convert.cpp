bool write_to_file(std::string name, int width, int height, bool flipY) {
    std::string str = name + ".hdr";
    GLfloat* pixels = (GLfloat*)malloc(width * height * sizeof(GLfloat) * 4);
    glReadPixels(0, 0, width, height, GL_RGBA, GL_FLOAT, pixels);
    // save to HDR
    stbi_flip_vertically_on_write(flipY);
    stbi_write_hdr(str.c_str(), width, height, 4, pixels);
    // save to RGBE
    stbi_uc *buf = (stbi_uc*)malloc(width * height * sizeof(stbi_uc) * 4);
    for (int i = 0; i < width * height; i++)
        stbiw__linear_to_rgbe(buf + i * 4, pixels + i * 4);
    free(pixels);
    str = name + ".hdr.png";
    stbi_flip_vertically_on_write(flipY);
    stbi_write_png(str.c_str(), width, height, 4, buf, 0);
    free(buf);
    // read HDR
    int nrComponents;
    str = name + ".hdr";
    stbi_set_flip_vertically_on_load(flipY);
    buf = stbi_load(str.c_str(), &width, &height, &nrComponents, 0);
    // save to LDR
    str = name + ".jpg";
    stbi_flip_vertically_on_write(flipY);
    stbi_write_jpg(str.c_str(), width, height, nrComponents, buf, 0);
    stbi_image_free(buf);
    return true;
}
